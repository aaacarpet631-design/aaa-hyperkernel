/*
 * AAA Software Factory Card — "build a review dashboard" as a governed build
 * request.
 *
 * The Copilot does not silently write production code. This card turns a build
 * request into a SPEC and a governed proposal (Council Governance), and tracks
 * the build lifecycle honestly: spec → files → tests → PR. Until the Genesis
 * Foundry / a human actually builds it, files/tests/PR are reported as `pending`
 * (insufficient_data), never a fabricated PR link. Approval is required to start.
 */
;(function (global) {
  'use strict';

  function governance() { return global.AAA_COUNCIL_GOVERNANCE; }

  function deriveSpec(text) {
    const t = String(text == null ? '' : text);
    const m = t.match(/\b(?:build|create|make|ship)\b\s+(?:a|an|the)?\s*(.+)/i);
    const what = m ? m[1].trim().replace(/[.?!]+$/, '') : t.trim();
    return { artifact: what || 'requested software', requested: t };
  }

  const Card = {
    deriveSpec: deriveSpec,

    /** Build a governed build-request card from the owner's text. */
    async build(text, opts) {
      const o = opts || {};
      const spec = deriveSpec(text);
      let approvalPackage = null;
      if (governance()) {
        const pr = await governance().propose('innovation', { council: 'software_factory', action: 'build: ' + spec.artifact, rationale: o.rationale || 'Owner requested a new build via the Chat Canvas.', confidence: 0.6 });
        approvalPackage = pr.recommendation || null;
      }
      return {
        type: 'software_factory', title: 'Build: ' + spec.artifact, status: 'spec_proposed',
        spec: spec,
        files: { status: 'pending', note: 'Generated by the Genesis Foundry after approval.' },
        tests: { status: 'pending' },
        prStatus: 'not_started',
        governanceRequired: true,
        approvalPackage: approvalPackage,
        nextStep: 'Approve to dispatch the build to the Genesis Foundry.'
      };
    }
  };

  global.AAA_SOFTWARE_FACTORY_CARD = Card;
})(typeof window !== 'undefined' ? window : this);
